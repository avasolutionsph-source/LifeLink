// Helper function to calculate stats
export const getMockStats = () => {
  const totalDonors = mockDonors.length;
  const eligibleDonors = mockDonors.filter(d => d.Eligibility_Status === 'Eligible').length;

  const totalUnits = mockInventory.reduce((sum, item) => sum + item.Quantity, 0);

  const today = new Date();
  const expiringSoon = mockInventory.filter(item => {
    const expiryDate = new Date(item.Expiry_Date);
    const daysUntilExpiry = Math.ceil((expiryDate - today) / (1000 * 60 * 60 * 24));
    return daysUntilExpiry <= 30 && item.Status === 'Available';
  }).length;

  const pendingRequests = mockRequests.filter(r => r.Status === 'Pending').length;

  const inventoryByType = ['A+', 'A-', 'B+', 'B-', 'AB+', 'AB-', 'O+', 'O-'].map(type => ({
    Blood_Type: type,
    total: mockInventory.filter(i => i.Blood_Type === type).reduce((sum, item) => sum + item.Quantity, 0)
  }));

  return {
    donors: { total: totalDonors, eligible: eligibleDonors },
    inventory: { totalUnits, expiringSoon },
    requests: { pendingCount: pendingRequests },
    donations: { total: mockDonations.length },
    inventoryByType
  };
};
