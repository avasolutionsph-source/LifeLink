// Helper function to calculate comprehensive stats
export const getLifeLinkStats = () => {
  const totalDonors = mockDonors.length;
  const eligibleBloodDonors = mockDonors.filter(d => d.Blood_Eligibility_Status === 'Eligible' && d.Willing_Blood_Donor).length;
  const registeredOrganDonors = mockDonors.filter(d => d.Willing_Organ_Donor && d.Organ_Donor_Status !== 'Inactive').length;
  const registeredTissueDonors = mockDonors.filter(d => d.Willing_Tissue_Donor).length;

  const totalBloodUnits = mockBloodInventory.reduce((sum, item) => sum + item.Quantity, 0);

  const today = new Date();
  const bloodExpiringSoon = mockBloodInventory.filter(item => {
    const expiryDate = new Date(item.Expiry_Date);
    const daysUntilExpiry = Math.ceil((expiryDate - today) / (1000 * 60 * 60 * 24));
    return daysUntilExpiry <= 30 && item.Status === 'Available';
  }).length;

  const tissuesExpiringSoon = mockTissues.filter(item => {
    const expiryDate = new Date(item.Expiry_Date);
    const daysUntilExpiry = Math.ceil((expiryDate - today) / (1000 * 60 * 60 * 24));
    return daysUntilExpiry <= 90 && item.Status === 'Available';
  }).length;

  const pendingRequests = mockRequests.filter(r => r.Status === 'Pending').length;
  const criticalRequests = mockRequests.filter(r => r.Urgency_Level === 'Critical' || r.Urgency_Level === 'Life-Threatening').length;

  const availableOrgans = mockOrgans.filter(o => o.Organ_Status === 'Available').length;
  const matchedOrgans = mockOrgans.filter(o => o.Organ_Status === 'Matched').length;

  const availableTissues = mockTissues.filter(t => t.Status === 'Available').length;

  const activeRecipients = mockRecipients.filter(r => r.Waitlist_Status === 'Active').length;
  const matchedRecipients = mockRecipients.filter(r => r.Waitlist_Status === 'Matched').length;

  const bloodByType = ['A+', 'A-', 'B+', 'B-', 'AB+', 'AB-', 'O+', 'O-'].map(type => ({
    Blood_Type: type,
    total: mockBloodInventory.filter(i => i.Blood_Type === type).reduce((sum, item) => sum + item.Quantity, 0)
  }));

  return {
    donors: {
      total: totalDonors,
      eligibleBlood: eligibleBloodDonors,
      registeredOrgan: registeredOrganDonors,
      registeredTissue: registeredTissueDonors
    },
    blood: {
      totalUnits: totalBloodUnits,
      expiringSoon: bloodExpiringSoon,
      byType: bloodByType
    },
    organs: {
      available: availableOrgans,
      matched: matchedOrgans,
      total: mockOrgans.length
    },
    tissues: {
      available: availableTissues,
      expiringSoon: tissuesExpiringSoon,
      total: mockTissues.length
    },
    recipients: {
      active: activeRecipients,
      matched: matchedRecipients,
      total: mockRecipients.length
    },
    requests: {
      pendingCount: pendingRequests,
      criticalCount: criticalRequests
    },
    donations: {
      total: mockDonations.length
    }
  };
};
